namespace Concepto\Sises\ApplicationBundle\Model;


class EntregaBeneficioQuery {

    /**
     * @var string
     */
    protected $id;

    /**
     * @var \DateTime
     */
    protected $fecha;

    /**
     * @return \DateTime
     */
    public function getFecha()
    {
        return $this->fecha;
    }

    /**
     * @param \DateTime $fecha
     */
    public function setFecha($fecha)
    {
        $this->fecha = $fecha;
    }

    /**
     * @return string
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param string $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }
} 