namespace Concepto\Sises\ApplicationBundle\Entity;

use Doctrine\ORM\Mapping\Column;
use Doctrine\ORM\Mapping\Entity;
use Doctrine\ORM\Mapping\GeneratedValue;
use Doctrine\ORM\Mapping\Id;
use Doctrine\ORM\Mapping\Table;

/**
 * Class Empresa
 * @package Concepto\Sises\ApplicationBundle\Entity
 * @Entity()
 * @Table(name="empresa")
 */
class Empresa {
    /**
     * @var string
     * @Id()
     * @Column(name="id", length=16)
     * @GeneratedValue(strategy="UUID")
     */
    protected $id;

    /**
     * @var string
     * @Column(name="nombre", length=250, nullable=false)
     */
    protected $nombre;

    /**
     * @var string
     * @Column(name="nit", length=15, unique=true, nullable=false)
     */
    protected $nit;

    /**
     * @var string
     * @Column(name="logo", length=255, nullable=true)
     */
    protected $logo;

    /**
     * @var string
     * @Column(name="telefono", type="string", nullable=true)
     */
    protected $telefono;

    /**
     * @var string
     * @Column(name="direccion", type="string", nullable=true, length=255)
     */
    protected $direccion;

    /**
     * @var string
     * @Column(name="email", type="string", nullable=true, length=255)
     */
    protected $email;

    /**
     * @return string
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getNombre()
    {
        return $this->nombre;
    }

    /**
     * @param string $nombre
     */
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;
    }

    /**
     * @return string
     */
    public function getLogo()
    {
        return $this->logo;
    }

    /**
     * @param string $logo
     */
    public function setLogo($logo)
    {
        $this->logo = $logo;
    }

    /**
     * @return string
     */
    public function getNit()
    {
        return $this->nit;
    }

    /**
     * @param string $nit
     */
    public function setNit($nit)
    {
        $this->nit = $nit;
    }

    /**
     * @return string
     */
    public function getTelefono()
    {
        return $this->telefono;
    }

    /**
     * @param string $telefono
     */
    public function setTelefono($telefono)
    {
        $this->telefono = $telefono;
    }

    /**
     * @return string
     */
    public function getDireccion()
    {
        return $this->direccion;
    }

    /**
     * @param string $direccion
     */
    public function setDireccion($direccion)
    {
        $this->direccion = $direccion;
    }

    /**
     * @return string
     */
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * @param string $email
     */
    public function setEmail($email)
    {
        $this->email = $email;
    }
}